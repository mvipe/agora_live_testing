    private fun initializeAndJoinChannel() {
        Log.i(TAG, "‚öôÔ∏è ==== INITIALIZE AND JOIN START ====")

        try {
            val config = RtcEngineConfig().apply {
                mContext = baseContext
                mAppId = APP_ID
                mEventHandler = mRtcEventHandler
            }
            Log.i(TAG, "‚öôÔ∏è RtcEngineConfig created with App ID: $APP_ID")

            mRtcEngine = RtcEngine.create(config)
            Log.i(TAG, "‚öôÔ∏è RtcEngine created successfully: ${mRtcEngine != null}")

            mRtcEngine?.let { engine ->
                Log.i(TAG, "‚öôÔ∏è Configuring engine...")

                // Basic audio configuration - simplified
                val audioResult = engine.enableAudio()
                Log.i(TAG, "‚öôÔ∏è Enable audio result: $audioResult")

                // Use default audio profile first
                val audioProfileResult = engine.setAudioProfile(Constants.AUDIO_PROFILE_DEFAULT)
                Log.i(TAG, "‚öôÔ∏è Audio profile result: $audioProfileResult")

                // Normal volume (100%)
                val volumeResult = engine.adjustPlaybackSignalVolume(100)
                Log.i(TAG, "‚öôÔ∏è Volume adjustment result: $volumeResult")

                val speakerResult = engine.setDefaultAudioRoutetoSpeakerphone(true)
                Log.i(TAG, "‚öôÔ∏è Speaker route result: $speakerResult")

                val volumeIndicationResult = engine.enableAudioVolumeIndication(200, 3, true)
                Log.i(TAG, "‚öôÔ∏è Volume indication result: $volumeIndicationResult")

                // Video configuration
                val videoResult = engine.enableVideo()
                Log.i(TAG, "‚öôÔ∏è Enable video result: $videoResult")

                // Channel configuration
                val channelProfileResult = engine.setChannelProfile(Constants.CHANNEL_PROFILE_LIVE_BROADCASTING)
                Log.i(TAG, "‚öôÔ∏è Channel profile result: $channelProfileResult")

                val clientRoleResult = engine.setClientRole(Constants.CLIENT_ROLE_AUDIENCE)
                Log.i(TAG, "‚öôÔ∏è Client role result: $clientRoleResult")


                val options = ChannelMediaOptions().apply {
                    channelProfile = Constants.CHANNEL_PROFILE_LIVE_BROADCASTING
                    clientRoleType = Constants.CLIENT_ROLE_AUDIENCE
                    autoSubscribeAudio = true
                    autoSubscribeVideo = true
                    publishMicrophoneTrack = false  // Explicit for audience
                    publishCameraTrack = false      // Explicit for audience
                }
                Log.i(TAG, "‚öôÔ∏è Channel options created - autoSubscribeAudio: ${options.autoSubscribeAudio}, autoSubscribeVideo: ${options.autoSubscribeVideo}")

                Log.i(TAG, "‚öôÔ∏è About to join channel: $channelName with token: $TOKEN")
                // Use UID 0 to let Agora assign a unique UID
                val joinResult = engine.joinChannel(TOKEN, channelName, 1, options)
                Log.i(TAG, "‚öôÔ∏è Join channel result: $joinResult")

            } ?: run {
                Log.e(TAG, "‚öôÔ∏è Failed to create RtcEngine!")
            }

            Log.i(TAG, "‚öôÔ∏è ==== INITIALIZE AND JOIN END ====")

        } catch (e: Exception) {
            Log.e(TAG, "‚öôÔ∏è ==== EXCEPTION IN INITIALIZE ====")
            Log.e(TAG, "‚öôÔ∏è Exception: ${e.message}")
            Log.e(TAG, "‚öôÔ∏è Stack trace: ${e.stackTrace.contentToString()}")
            Log.e(TAG, "‚öôÔ∏è ==== END EXCEPTION ====")

            Toast.makeText(this, "Error: ${e.message}", Toast.LENGTH_SHORT).show()
        }
    }

    private fun setupRemoteVideo(uid: Int) {
        Log.i(TAG, "üì∫ ==== SETUP REMOTE VIDEO START ====")
        Log.i(TAG, "üì∫ Setting up video for UID: $uid")
        Log.i(TAG, "üì∫ Container exists: ${remoteVideoContainer != null}")
        Log.i(TAG, "üì∫ Container current children: ${remoteVideoContainer?.childCount}")

        try {
            val surfaceView = RtcEngine.CreateRendererView(baseContext)
            Log.i(TAG, "üì∫ SurfaceView created: ${surfaceView != null}")
            Log.i(TAG, "üì∫ SurfaceView class: ${surfaceView?.javaClass?.name}")

            remoteVideoContainer?.let { container ->
                Log.i(TAG, "üì∫ Removing all existing views...")
                container.removeAllViews()
                Log.i(TAG, "üì∫ Children after removal: ${container.childCount}")

                Log.i(TAG, "üì∫ Adding SurfaceView to container...")
                container.addView(surfaceView)
                Log.i(TAG, "üì∫ Children after adding: ${container.childCount}")

                Log.i(TAG, "üì∫ Container dimensions: ${container.width}x${container.height}")
                Log.i(TAG, "üì∫ Container visibility: ${container.visibility}")

            } ?: run {
                Log.e(TAG, "üì∫ Container is null!")
            }

            val remoteVideoCanvas = VideoCanvas(surfaceView, VideoCanvas.RENDER_MODE_HIDDEN, uid)
            Log.i(TAG, "üì∫ VideoCanvas created - UID: ${remoteVideoCanvas.uid}, RenderMode: ${remoteVideoCanvas.renderMode}")

            val setupResult = mRtcEngine?.setupRemoteVideo(remoteVideoCanvas)
            Log.i(TAG, "üì∫ setupRemoteVideo result: $setupResult")

            // Additional surface view checks
            surfaceView?.let { sv ->
                Log.i(TAG, "üì∫ SurfaceView holder: ${sv.holder}")
                Log.i(TAG, "üì∫ SurfaceView visibility: ${sv.visibility}")
                Log.i(TAG, "üì∫ SurfaceView layout params: ${sv.layoutParams}")

                sv.post {
                    Log.i(TAG, "üì∫ SurfaceView final dimensions: ${sv.width}x${sv.height}")
                }
            }

        } catch (e: Exception) {
            Log.e(TAG, "üì∫ ==== EXCEPTION IN SETUP REMOTE VIDEO ====")
            Log.e(TAG, "üì∫ Exception: ${e.message}")
            Log.e(TAG, "üì∫ Stack trace: ${e.stackTrace.contentToString()}")
            Log.e(TAG, "üì∫ ==== END EXCEPTION ====")
        }

        Log.i(TAG, "üì∫ ==== SETUP REMOTE VIDEO END ====")
    }

    private fun setupClickListeners() {
        btnMuteAudio?.setOnClickListener {
            audioMuted = !audioMuted
            mRtcEngine?.muteAllRemoteAudioStreams(audioMuted)
            btnMuteAudio?.text = if (audioMuted) "üîá Unmute Audio" else "üîä Mute Audio"
            tvAudioIndicator?.text = if (audioMuted) "üîá MUTED" else "üîä ON"

            val message = if (audioMuted) "Audio muted" else "Audio unmuted"
            Toast.makeText(this, message, Toast.LENGTH_SHORT).show()
        }

        btnFullscreen?.setOnClickListener {
            // Toggle fullscreen (simplified version)
            isFullscreen = !isFullscreen
            if (isFullscreen) {
                // Hide system UI for fullscreen
                window.decorView.systemUiVisibility = (
                        android.view.View.SYSTEM_UI_FLAG_IMMERSIVE_STICKY
                                or android.view.View.SYSTEM_UI_FLAG_HIDE_NAVIGATION
                                or android.view.View.SYSTEM_UI_FLAG_FULLSCREEN
                        )
                btnFullscreen?.text = "‚õ∂ Exit Fullscreen"
            } else {
                // Show system UI
                window.decorView.systemUiVisibility = android.view.View.SYSTEM_UI_FLAG_VISIBLE
                btnFullscreen?.text = "‚õ∂ Fullscreen"
            }
        }

        btnLeaveChannel?.setOnClickListener {
            androidx.appcompat.app.AlertDialog.Builder(this)
                .setTitle("Leave Stream")
                .setMessage("Are you sure you want to leave this live stream?")
                .setPositiveButton("Leave") { _, _ ->
                    finish()
                }
                .setNegativeButton("Cancel", null)
                .show()
        }
    }

